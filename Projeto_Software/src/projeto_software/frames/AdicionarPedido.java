    public void definirNumFatias() {

        String tamanho = comboTamanho.getSelectedItem() + "";

        switch (tamanho) {
            case "PEQUENA" ->
                txtfNumFatias.setText("4");

            case "MÉDIA" ->
                txtfNumFatias.setText("6");
            case "GRANDE" ->
                txtfNumFatias.setText("8");
            default ->
                txtfNumFatias.setText("");

        }
    }

